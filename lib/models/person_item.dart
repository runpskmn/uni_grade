class PersonItem{
  final int id;
  final String firstName;
  final String lastName;
  final String email;
  final String phoneNo;
  final String birthday;
  final String status;

  PersonItem({
    required this.id,
    required this.firstName,
    required this.lastName,
    required this.email,
    required this.phoneNo,
    required this.birthday,
    required this.status
  });
}